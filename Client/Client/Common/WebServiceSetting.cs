using System;
using System.Collections.Generic;
using System.Text;

namespace Client.Common
{
    class WebServiceSetting
    {
    }

    /// <summary>
    /// Service的配置
    /// </summary>
    public class WebSetting
    {
        /// <summary>
        /// 服务名称，配置文件对应的名称
        /// </summary>
        public string ServiceSettingName { get; set; }

        /// <summary>
        /// IP地址 / 网址
        /// </summary>
        public string IPOrWebAddress { get; set; }

        /// <summary>
        /// 端口
        /// </summary>
        public string Port { get; set; }

        /// <summary>
        /// 应用程序
        /// </summary>
        public string AppName { get; set; }

        public WebSetting(string serviceSettingName, string ipOrWebAddress, string port, string appName)
        {
            this.ServiceSettingName = serviceSettingName;
            this.IPOrWebAddress = ipOrWebAddress;
            this.Port = port;
            this.AppName = appName;
        }
    }

}
